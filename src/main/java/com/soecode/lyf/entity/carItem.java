package com.soecode.lyf.entity;

public class carItem {
    private int id;
    private int uid;
    private int pid;
    private int count1;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getCount() {
        return count1;
    }

    public void setCount(int count) {
        this.count1 = count;
    }
}
